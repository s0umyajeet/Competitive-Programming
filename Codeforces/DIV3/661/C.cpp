#include <bits/stdc++.h>
#define int     long long
#define YES     cout << "YES" << endl
#define NO      cout << "NO" << endl
#define coutans cout << "ans: "     //debug
#define endl    "\n"
using namespace std;

int32_t main() {
        ios_base::sync_with_stdio(0);
        cin.tie(0);
        
        int t;
        cin >> t;

        while(t--) {
                int n;
                cin >> n;
                vector<int> v(n);
                for (auto &x : v) cin >> x;

                map<int, int> team;
                for (int i = 0; i < n; i++) {
                        for (int j = i + 1; j < n; j++) {
                                // cout << v[i] + v[j] << endl;
                                team[v[i] + v[j]]++;
                        }
                        // cout << v[i] << " " << v[j] << endl;
                }
                int maxx = -999;
                for (auto itr = team.begin(); itr != team.end(); itr++) {
                        maxx = max(itr->second, maxx);
                }
                coutans << maxx << endl;
        }
}